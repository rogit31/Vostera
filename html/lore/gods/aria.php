                <h1 id="1">Aria, The Rebirth</h1>
                <p>Aria is the goddess of spring, of art, of rebirth, of politics, of change and metamorphosis. She is the only known of god the <a href="ashirianpantheon.php" class="crosslink">Ashirian Pantheon</a> that is of mortal origin. </p>
                <h2>Origin</h2>
                <hr class="solid">
                <p>The legend says that in the olden days, Layla took on a mortal form to indulge in a hunt on a moonless night, enjoying the absence of her sister to the fullest. On her hunt, she witnessed a mortal sitting and writing by candlelight on the edge of a lake. It wasn't the first time that Layla saw a mortal, but it was the first time she's seen one so beautiful. Her thick wavy hair tied in a ponytail reflected the amber light of the candle like waves under the moonlight. Beyond her appearance, the mortal did something Layla had never witnessed before. She sung. And her voice, clear as the rivers of the Shimmerpeak alps, carried the mortal's song through the woods. Entranced, Layla took the form of a rabbit to sneak up on her and listen to the mortal's song from up close. For months, Layla would take on the form of different animals to come back on the moonless nights to revel in the song and poetry of this mortal.</p>

                <p>Eventually, Layla couldn't take the secrecy of her identity anymore and chose to reveal herself in her humanoid form. The mortal, out of surprise, fell into the lake she was standing next to. Layla rescued her and found out her name, Aria. From there, their love grew and flourished through the years, mostly as a secret from the other gods. They fell in love just as Lua and Ma'at did, hard and forever.</p>

                <p>But, as Aria was mortal, came the time like all other mortals to meet their fate, Ma'at visited her in the night and cut the thread of fate that tied her to the material plane. On her way to the planes beyond, Aria sang. She sang about her undying, powerful and passionate love for Layla. Her song so powerful, stories so captivating, paintings so beautiful and expressive Ma'at was reminded of his love for Lua and could not help but think of what he would do if Lua was mortal. When Layla came to him to beg for him to release her mortal form from his web, Ma'at's heart wavered. Many people have tried to beg Ma'at for their loved ones, but Aria didn't beg. She simply sang. Ma'at couldn't release her mortal form but he proposed a different solution. Aria was to sing to the gods as she sang for Layla and Ma'at before them, and Ma'at could push for her to be edified into a goddess herself.</p>

                <p>And so Ma'at showed the celestial council Aria's creations. As the other gods before them, the gods where struck by the sheer talent, artistry and passion of the mortal. Mila was particularly enamored, calling her works a testament to the existence of true love. Very little debate had to be done by the council as she was edified into a goddess. Aria became the first mortal to survive Ma'at's thread and the first mortal to ascend to godship.</p>

                <h2 id="2">Philosophy</h2>
                <hr class="solid">
                <p>With her divine gift, Aria breathed life into the world through her art, painting the skies with vibrant hues and composing melodies that echoed through the hearts of mortals. She nurtured the spirit of creativity and urged mortals to embrace the ever-changing rhythms of existence. In the pantheon, Aria's presence was celebrated through grand festivals and gatherings where artists, musicians, and poets would gather to pay homage to her divine inspiration. Her temple stood as a sanctuary for creators, a place where their works could flourish and their souls find solace. Aria's influence permeated every aspect of artistic expression, encouraging mortals to embrace their inner muse, to fearlessly explore the depths of their creativity, and to recognize the profound beauty and transformative power of art, music, spring, and the ever-evolving tapestry of life. Through Layla's love and the transformation of Aric into Aria, a profound connection between the realms of the divine and the mortal was forged, forever reminding humanity that the arts hold the power to ignite change, to express the depths of emotion, and to weave threads of beauty and inspiration throughout the world.</p>

