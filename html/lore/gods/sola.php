                <h1>Sola, The Sunmother</h1>
                <p>The Sunmother, Sola, is the goddess of the sun and its light. She is associated with ideas of peace, community, prosperity and fertility.</p>


                <blockquote>"Like children in their mother's arms, we all bathe in Sola's light and protection."</blockquote>


                <h2 id="2">Origin</h2>
                <hr class="solid">
                <p>Born from <a href="person:cee81bfb-f1a2-4006-ac4f-c7b5d7a21b5b">The Godfather, Kaos</a> in the primordial era amongst the thousands of the Godfather's children, the stars and planets.</p>

                <h2 id="3">Relations</h2>
                <hr class="solid">
                <p>It is said that Sola is at the origin of life itself. Despite her thousands of siblings throughout the cosmos, Sola felt lonely because of the massive distance separating them. To remedy to her solitude, Sola bathed the planets around her in her light, and gave Vostera the light and warmth it needed for life. She also gave birth to <a class="crosslink" href="lua.php">The Tidecaller, Lua</a>, and <a class="crosslink" href="layla.php">Layla, Goddess of the Night</a>. In her primordial, raw light, the first being of Vostera appeared, the <a class="crosslink" href="../people/titans.php">Titans</a>. Gigantic creatures of magic fueled by Sola's power and Vostera's raw magic, they were incapable of communicating or reproducing and fought over territory and access to Sola's light. Disappointed and angry, Sola went dormant during <a class="crosslink" href="../history/thousandyearwinter.php">The Thousand Year Winter</a>. Vostera's life perished, and the last titans that were left standing after <a class="crosslink" href="../history/titanicwar.php">The Titanic War</a> went dormant.</p>

                <p>When Sola awoke, she once again bathed Vostera in her light, giving life on Vostera a second chance. As her light cradled Vostera, it is said that different creatures rose from different environments. From the plains rose the creatures of men, from the woods the elves, the mountains the dwarves, the swamps the orcs, the volcanoes the dragonborn and so on. From the bodies of old titans came beings of great magical power, including <a class="crosslink" href="../people/planewalker.php">The Planewalker</a>. As a gift to Vosterity, she blessed them with the ability of conscious thought and speech, hoping that Vosterity would worship her and create bonds with each other instead of waging war for her light. As she used her powers to give this gift, she was helped by her brother, <a class="crosslink" href="vero.php">The Truth, Vero</a>.</p>

                <h2 id="4">Sola's Cult</h2>
                <hr class="solid">
                <p>Sola is omnipresent throughout history as an object of worship, from species across Vostera. Her worship often ties to her ideas of peace and prosperity but has also been found to include the physical properties of the sun and its light for agricultural purposes, among others. Sola's followers are called <a class="crosslink" href="../organizations/solistchurch.php">Solists</a>. Her cult is typically pacifist and hedonistic in nature.</p>

                <h2 id="5">Character and Philosophy</h2>
                <hr class="solid">
                <p>She is said to be a fair deity, that brings the light in times of dire need and takes it away as punishment to those who have angered her. Typically, Sola is thought of as a motherly figure to all living beings, caring, warm and protective.</p>

                <h2 id="6">Representation</h2>
                <hr class="solid">
                <p>In paintings, statues, and other representations, Sola is typically represented as a dark-skinned woman with fiery red hair and pupil-less golden eyes. She can sometimes be seen with a crown of sunflowers and a phoenix on her.

