                <h1>Lua, The Tidecaller</h1>
                <p>The Tidecaller, Lua, is the goddess of the moon, of the tides, of birth, of fertility, and of change.</p>
                <blockquote>"Whereas Sola is gold; Lua is silver, and Layla is obsidian."</blockquote>

                <h2>Origin</h2>
                <hr class="solid">
                <p>According to the creation myth, she was born from <a class="crosslink" href="sola.php">The Sunmother, Sola</a> at the same time as the <a class="crosslink" href="../people/titans.php">Titans</a> and her Sister <a class="crosslink" href="layla.php">Layla, Goddess of the Night</a>.</p>

                <h2>Relationships</h2>
                <hr class="solid">
                <p>Lua is the daughter of <a class="crosslink" href="sola.php">The Sunmother, Sola</a>, and they are as a pair perceived as two sides of the same coin.</p>

                <p>Lua is also the sister to <a class="crosslink" href="layla.php">Layla, Goddess of the Night</a>, the goddess of the night and of death. They are described as being in an eternal struggle for Vostera's skies during nighttime, creating the moon cycle.</p>

                <h2>Lua's Cult</h2>
                <hr class="solid">
                <p>The Tidecaller has a significant following all over Vostera, called the <a class="crosslink" href="../organizations/silvermoonchurch.php">Silver Moon Church</a>. The Silver Moon is most present on coastal cities, where the tides and the seas can hold strong cultural significance over the habitants, but remains worshipped even in rural landlocked. The worship of Lua is generally tied to ideas of cyclical change, the cycle of life, storms, navigation, the moon, the seas, and of protection at night, in the absence of Sola's light. The Silver Moon has churches across Vostera, and many of them function as birth centers and small nurseries on top of being places of worship and rest.</p>

                <h2>Character and Philosophy</h2>
                <hr class="solid">
                <p>Lua, much like Sola, is seen as a protective figure to Vosterians. But whereas Sola nurtured life on Vostera, Lua protects from the dangers of the night by giving a bit of its light, scaring off beasts and supernatural alike. She protects ships and sailors from crashing into the shore by illuminating the inky seas, gives reference to travelers and hunters and calls the tide for ships to set sea. As the goddess of fertility, women across Vostera track their own cycle using the moon's and pray to her when trying for children or birthing them. Nevertheless she is still seen as fierce and full of primal energy when she needs to bring the storm. Lua's believers generally tend to see change and life as a natural part of the cosmic cycle.</p>

                <h2>Representation</h2>
                <hr class="solid">
                <p>When represented fully, she is seen as a woman with pale ivory skin and long, thick teal hair. She is often seen holding a lantern. She is usually seen wearing plain clothes though she is sometimes represented with scale armor.</p>

                <h2>Symbol</h2>
                <hr class="solid">
                <p>She is symbolized by the crescent moon.</p>
