                <hr class="solid">
                <p>The Fateweaver is also known to be the lover of Lua, the Tidecaller and goddess of the moon. Their relationship is a complex one, as the Fateweaver's role in determining death often puts them at odds with Lua's role as the goddess of birth and fertility as well as the daughter of <a class="crosslink" href="sola.php">The Sunmother, Sola</a>. However, their love for each other is said to be deep and unbreakable, and some believe that their union is what keeps the balance of life and death in Vostera. Of their union, was born <a class="crosslink" href="mila.php">Mila, The Everblazing Heart</a>.</p>

                <h2 id="3">Symbolism</h2>
                <hr class="solid">
                <p>Symbols associated with the Fateweaver include weaving tools such as looms and spindles, as well as threads and cords that represent the strands of fate being woven together. The Fateweaver's followers often wear black robes or clothing adorned with these symbols, and some even dye their hair or tattoo their skins in webs to honor the deity.</p>

                <p>In visual representations, the Fateweaver is represented as a giant spider-like creature with multiple arms that spin webs, symbolizing the threads of fate. Each thread is of a different color, representing the different paths a person's life can take. The Fateweaver's body is black and shimmering, as if made of obsidian or onyx, and its eyes are bright white, representing the moon that its lover Lua presides over. Its many legs are adorned with intricate, geometric patterns, symbolizing the complex nature of fate and the interwoven paths that all living beings follow.</p>

                <h2 id="4">Cult</h2>
                <hr class="solid">
                <p>Within the cult of the Fateweaver, those who go through extensive training and years of monasterial work become known as the Weavers. These individuals are highly revered within the cult and are responsible for interpreting and reading the fates of those who seek their guidance. However, the actual weaving of a person's fate is believed to be done only by the Fateweaver itself.</p>

                <p>The Weavers spend years studying the intricacies of fate, learning how to read the complex web of threads that represent a person's life. They are highly skilled in divination and can provide detailed insights into a person's past, present, and future.</p>

                <p>It is said that the Weavers are able to communicate with the Fateweaver directly, seeking guidance and insight from the deity itself. However, only the Fateweaver is capable of actually weaving a person's fate into the fabric of the cosmos.</p>

                <p>Despite their importance within the cult, the Weavers are considered to be mere mortals and are subservient to the will of the Fateweaver. They are deeply loyal to the deity and will do anything to protect and serve its interests.</p>

                <p>Other people who come to the Fateweaver for guidance are people who may fear their own mortality, who want to connect with their loved ones who may be in Ma'at's hands already, or who seek guidance with their fate.</p>

                <p>Overall, the Fateweaver is a powerful and feared deity in Vostera, but also one that is respected and honored for their role in the natural cycle of life and death.</p>

