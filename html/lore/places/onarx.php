                <h1>Onarx</h1>
                <p>Onarx is a city located on the east coast of the Ashir empire that is famous for it's high concentration of hunting and hunting-related economy, an extremely humid and warm climate, as well as it's aether-based anti-natural disaster system, or AANDS.</p>
                <h2 id="2">Geography</h2>
                <hr class="solid">
                <h3>Location</h3>
                <p>Onarx is situated on the east coast of the Eloran continent, and is right about on the equator. It is surrounded by the Anderan tropics, a thick jungle rich with an incredibly diverse fauna and flora. It is nested in a small bay on the dawnbreak coast, providing it with access to the Anderan ocean. It is also only two days travel away from the Kaan mountains, and a few more from Tandal peak, the tallest mountain in Eloran. On clear days, Tandal peak is visible from from high vantage points in Onarx.</p>
                <h3>Features</h3>
                <p>The city itself is hilly and filled with greenery, giving it the nickname the greenhouse. The Tandal river splits Onarx into two, east and west Onarx. It's tallest hill, the Brow, is the centre of the city and harbors most municipal buildings.</p>
                <h3>Climate</h3>
                <p>The climate in Onarx is warm and humid. It's proximity to the Kaan mountains and the Anderan Ocean makes it a concentrated bubble of humidity, that would be boiling if not for it's proximity to the ocean. It is humid, hot, and mostly rainy throughout the year. </p>
                <h2 id="3">History</h2>
                <hr class="solid">
                <p>It is said Onarx was founded by a small community of hunters who settled here because of the wide variety of fauna and flora as well as the proximity to the ocean, enabling them to hunt, gather, and fish all at once. To this day, Onarx remains a hotspot for hunters.</p>
                <p>Slowly over time, the city grew with more and more hunters and merchants seeing the city as a potential hub for expansion. After it's inception and before the empire, Onarx was a feudal city-state, controlled by the oldest successful hunter's guild. With the expansion of the empire, it was absorbed in 351 AP.</p>
                <p>Regular and devastating floods and earthquakes mark the different eras of the city's expansion, with clear and notable changes made to the cities neighborhoods and streets with every natural disaster. In 234 AP, the city had it's first recorded flood, destroying about half of the city and taking the lives of hundreds. In 450 AP, a massive earthquake shattered the city's infrastructure, killed thousands and took decades to rebuild. In 681 AP, the city started working on a aether-based natural disaster system that would alarm and prevent floods and earthquakes from sweeping the city to bits like in the past. Since the creation and the implementation of the AANDS in 732 AP, the city has drastically reduced the impact of natural disasters to the city.</p>
                <h2 id="4">Population</h2>
                <hr class="solid">
                <p>The population in Onarx is mostly human with some half elves, half orcs and halflings and gnomes. The population is relatively young. There is a bit of a divide in between the population in Onarx that is there for the hunting and the population that isn't.</p>
                <h2 id="5">Government and economy</h2>
                <hr class="solid">
                <p>The Onarx local government is similar to the other towns in the Ashir empire, with a mayor that answers to a regional parliament. The mayor of Onarx is Jalen Overdal.</p>
                <h2 id="6">Architecture</h2>
                <hr class="solid">
                <p>Because of the regular natural disasters, Onarx has rebuilt itself from the ground up multiple times over the centuries, resulting in a relatively modern architecture made mostly out of stone, marble, and clay bricks. Some neighborhoods in Onarx still retain older architectures of wood and hay, but those buildings are few and far in between and are attractions as relics of the past.</p>
                <h2 id="7">Economy</h2>
                <hr class="solid">
                <p>Most of Onarx's economy revolves around the trade, sale, consumption or transformation of hunted animals. The nearby Anderan Tropics are home to the highest concentration of creatures, animals, and monsters to be hunted in all of the Ashir Empire. As such, people come far and wide to find rare leathers, meats, pets, and other animal byproducts. Other than that, Onarx has some of its economy in tourism, construction, education and technology.</p>
                <h2 id="8">Culture</h2>
                <hr class="solid">
                <p>The tandal river dividing Onarx into two parts with different cultures. The west side of Onarx is more 'regular', with less of a focus on hunting and animal byproducts as opposed to the east side, which is entirely focused on it. Onarx's hunters are known as friendly, reckless, and loud. </p>
