                <h1 id="1">Aegur</h1>
                <h2 id="2">Geography</h2>
                <hr class="solid">
                <p>Aegur is situated almost entirely on the southern hemishpere, and is a large continent that has a variety of ecosystems and environments. The northern part of the country closest to the equator is covered in a thick jungle, host to an incredible diveristy of fauna and flora. The continent is split by the Shimmerpeak alps, a mountain chain that rises thousands of kilometers tall and is home to many glaciers and mountain peaks. </p>
                <p>To the east of the mountain chain, lies a relatively temperate and wet environment, that gets drier and colder the further south you go. To the west of this mountain chain, a rain shadow is cast and features a much drier environment. On this side of the mountains, the environment is desertic near the Giant's Canyon and is akin to a tundra further south. Beyond this, the western side of the Shimmerpeak alps is mostly compromised of dry fields. </p>
                <p> To the south of the main continent, features the Aegurian wildlands, an unexplored island hostile to all that step foot on it, covered in abrupt and barren cliffs and mountaineous jagged rocks. The Aegurian wildlands are supposedly home to some of the rarer species on Vostera.</p>
                <h2 id="3">History</h2>
                <hr class="solid">
                <p>Aegur is a land rich in natural resources, including fertile farmland, abundant forests, and vast mineral deposits. The people of Aegur were once united under a powerful monarchy, which ruled over the land for centuries. However, over time, the monarchy became corrupt and the people began to rebel. The resulting civil wars devastated the land, leaving it vulnerable to outside forces. In the early 700s, the Kyrenian Empire began to expand into Aegur, seeking to exploit its resources and establish a foothold in the region. The people of Aegur fought fiercely against the Kyrenian invaders, but were eventually overwhelmed.
                <p>For the next several centuries, Aegur was ruled by a succession of foreign powers. The Losdraeg Kingdom, which had long coveted Aegur's mineral wealth, seized control of the region in the mid-700s. The Losdraegs established a puppet government in Aegur, ruling through a series of corrupt and tyrannical governors.
                <p>Today, Aegur is a divided land, with various warlords and factions vying for control. The people of Aegur continue to suffer under the yoke of foreign oppression, and many dream of a day when their land will be free once more.</p>
                <h2 id="4">Countries and Regions</h2>
                <hr class="solid">
                <p>The continent of Aegur is currently composed of three distinct governmental bodies.</p>
                <h3>The Aegurian Confederation</h3>
                <p>The jungles from Nagan to Sal'Sol compromise the Aegurian Confederation, effectively forming a small country that used to be compromised of individual city-states. The Aegurian Confederation is recent, and has strong ties to the Ashirian empire though many are skeptical about how long this alliance may last.</p>
                <h3>The Kyrenian North</h3>
                <p>Much of the northern part of the continent is under the de facto control of the Kyrenian government. Officially, the borders extend from Camport to Nedanas, with Nedanas being the largest city of Kyrenia in Aegur, but there has been military presence of Kyrenia as far south as Kelfar. It is thought that San Bazal poses a strong barrier for the expansion of Kyrenia into Aegurian territory due to it's military might. </p>
                <h3>Feudal Aegur</h3>
                <h4>Governmental structure</h4>
                <p> Feudal Aegur is characterized by a decentralized system of governance, where power is dispersed among various lords and local rulers. The land is divided into fiefs, each ruled by a lord or noble, and these rulers owe allegiance to higher-ranking nobility or, in some cases, to the Kyrenian North. The political landscape is fragmented, with individual fiefdoms operating with a degree of autonomy.</p>

                <h4>Military and defense</h4>
                <p> The military structure in Feudal Aegur is largely feudal in nature. Lords are responsible for raising and maintaining their own forces, including knights, men-at-arms, and possibly conscripted peasants. In times of external threats or conflicts, these individual forces may be mobilized to form a collective defense, but the lack of a centralized military command often leads to coordination challenges.</p>

                <h4>Economy and agriculture</h4>
                <p> Feudal Aegur's economy is primarily agrarian, with each fiefdom being responsible for its own agricultural production. The fertile farmlands of Aegur contribute to the sustenance of local populations. Trade between fiefdoms and external regions may occur, but it's often subject to the goodwill of local lords </p>

                <h4>Social hierarchy</h4>
                <p> The social hierarchy in Feudal Aegur is rigid, following traditional feudal structures. At the top are the ruling nobility – lords and ladies – who hold significant land and influence. Below them are the vassals, knights, and peasants, each with defined roles and responsibilities. The rights and privileges of individuals are largely determined by their position within this hierarchy.</p>

                <h4>Cultural diversity</h4>
                <p> Feudal Aegur is a melting pot of cultures and races, reflecting the diverse demographics of the entire continent. Each fiefdom may have its own unique customs, traditions, and dialects. Many of the fiefdoms in Aegur are led by elves or half-elves, orcs or half-orcs, goliaths or tieflings. The coexistence of various races and ethnicities contributes to a rich tapestry of cultural exchange, although tensions and conflicts may arise due to differences in beliefs and practices.</p>

                <h4>Challenges and struggles</h4>
                <p> The decentralized nature of Feudal Aegur poses challenges in terms of effective governance and response to external threats. Feuding among lords for territory and resources is common, leading to internal conflicts that hinder collective efforts. The foreign presence of the Kyrenian North adds an additional layer of complexity, with some lords aligning themselves with Kyrenia while others resist foreign rule.</p>

                <h4>Aspirations for independence</h4>
                <p>Despite the challenges, there are whispers of a desire for independence and unity under a single banner. Many fiefdoms are considering joining the confederation, others are considering allying themselves with the Kyr. Many serfs of these fiefdoms also aspire to become free of the social hierarchy under which they are living, and unrest is common. Whether these aspirations will lead to a unified resistance or further fragmentation remains uncertain.
                </p>
