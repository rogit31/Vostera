                <h1>Port Dalia</h1>
                <p>Port Dalia is a large city on the eastern coast of Eloran. It is the main trading post in between the Ashir empire and the Geldan Dynasty and the Lora Kingdom. Its main industries are commerce and fishing, with most of the money being made there coming from trade. The city has a bit of a reputation of being dirty and dangerous, with an above average amount of thieves, underground markets and criminals roaming around.</p>
                <p>The town is split into several districts. The port district is the district where most of the action is happening, and is a cobblestone street by the bay lined with docks, boats and merchants.</p>
                <p>The market district is more next to it, which contains an open air market filled with tents as well as streets filled with brick and mortar stores filled with oddities and curious. These stores and tents vary from food, jewelry, textile, weapons, magical items, art, you name it, you can find it. </p>
                <p>South of the port district, you find the murktow. The murktow is a shady part of town where most do not venture at night. It is filled with criminal activity and none walk here alone. The city police have attempted to reprimand the activity in this area but are unsuccessful, as the constant flow of persons venturing in and out of the city make it very hard to keep people accountable. On top of that, the city mayor, <a href="../people/karishuhrat.php" class="crosslink">mayor Shuhrat</a> is much more concerned with economic issues and the health of the trade business than policing the murktow. This is where <a href="../organizations/underbelly.php" class="crosslink">the Underbelly</a> is situated. </p>

                <p>North of the market district is the government district of the town. This district is the nicest district in the city, is policed and clean and contains all the major government institutions of this town. Police offices, Academy offices, Townhouse and so on. Here the cobblestone and marble seems to be cleaner and whiter, making the town actually look quite nice.</p>

                <p>The town is separated by the Besal river and has a large gated and raised bridge connecting the two halves of the town.</p>
