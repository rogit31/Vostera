            <h1>The Soft Spot</h1>
            <p>The Soft Spot is the name given to The Underbelly's HQ. No one knows where it is but is reached through magical portal means.</p>

            <p>The building of the Underbelly headquarters is a sight to behold. The Soft Spot is located next to a rushing river providing the building with water. The entrance to the building is a massive wooden door that is reinforced with iron bands. The door is emblazoned with the Underbelly's symbol, the magpie holding a dagger in its beak.</p>
            
            <p>The inside smokes of leathers and a wood fire. The interior of the building is dimly lit with torches and lanterns, casting flickering shadows on the walls. The main hall is expansive, with a high ceiling and a large fireplace on one wall. The floor is made of rough-hewn stone, and there are several wooden tables and benches scattered throughout the room.</p>
            
            <p>To the left of the main hall, there is a small bar where members can purchase drinks and food. The bar is made of dark wood and is lined with bottles of various liquors. The bartender is <a href="#" class="crosslink">Gerald</a>.</p>
            
            <p>To the right of the main hall, there is a training area where members can practice their skills. The area is covered in mats and there are several wooden dummies set up for practicing combat.</p>
            
            <p>Beyond the main hall, there are several smaller rooms where members can rest or conduct business. The rooms are small and sparsely furnished, with simple wooden beds and chests for storing personal belongings.</p>
            
            <p>Overall, the Underbelly headquarters is a rough and rustic place, but it has a certain charm to it. The building and its furnishings are all practical and functional, but there is a sense of camaraderie and belonging that pervades the atmosphere.</p>
            
            <p>As you walk deeper into the Soft Spot, you pass through multiple hallways and chambers, each with its own purpose. There are training rooms for thieves to practice and hone their skills, and armories stocked with weapons and tools of the trade. The walls are adorned with posters advertising the latest scores, and blackboards with details of upcoming jobs.</p>
            
            <p>Eventually, you reach a small, unassuming door made of solid wood. The door is unmarked, but the Underbelly members know that it leads to the inner sanctum: the secret meeting room. Once inside, you find yourself in a dimly lit, circular room with a long, wooden table in the center. The walls are lined with shelves of books and artifacts, and the air is heavy with the scent of incense.</p>
            
            <p>But the Soft Spot is not just a place for business. It is also a place for friendly competition among the thieves of the Underbelly. A leaderboard hangs on the wall, listing the top thieves of the month, with their faces and names next to the value of the merchandise they have stolen. Thieves gather around the leaderboard, congratulating each other on their successful heists and plotting ways to steal even more valuable items. This competition is called the grand theft-off.</p>
            
            <p>Despite the criminal roots of the organization, business is not to be conducted on soft spot grounds and is one of the few rules that all members of the Underbelly abide by.</p>
            
            <p>In the Soft Spot, the Underbelly members come together to plan, trade, and compete, all under the watchful eye of Raoul Ragimund, the leader of the organization.</p>
            
