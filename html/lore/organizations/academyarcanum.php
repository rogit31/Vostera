                    <h1>The Academy Arcanum</h1>
                    <p>The Academy Arcanum, also known as the academy, is a cross-continental school of magic, as well as the organization in charge of the regulation of magic. The academy is divided into three departments: education and research, peacekeeping, and intelligence. The academy was founded in 178 AP. It's main objective is the "safe education and protection of Vosterians from the arcane arts".</p>

                    <h2 id="2">Origin</h2>
                    <hr class="solid">
                    <p>The Academy Arcanum was created by <a href="../people/nolaashir.html" class="crosslink">Nola Ashir</a> as a way to educate spellweavers on how to manipulate aether into useful spells, police the use of magic from those who use it for harmful things, protect the powerless from spellweavers and expand its own influence. Since then, the empire has adopted the academy as its de facto magical police, incorporating it into the highest level of Ashirian politics and legislature.</p>

                    <h2 id="3">Education and Research</h2>
                    <hr class="solid">
                    <p>The academy is first and foremost, a place of learning of the arcane arts. The academy has schools and universities across Vostera, teaching those who attend magic. There are a few ways to enter the Academy. Passing a written test and a referal as well as paying expensive fees. These talented spellweavers are recruited directly from families in exchange for free tuition and free access to Academy facilities. What happens behind academy walls is a well guarded secret, but students end up in one of the three academy branches as Academy graduates for life.</p>

                    <h2 id="4">Peacekeeping</h2>
                    <hr class="solid">
                    <p>The peacekeeping branch of the academy is a private military unit organization that is hired mostly by the Ashir empire. The peacekeepers are seen as a force to be reckoned with worldwide. Officially, its interests are aligned with the peace and general well-being of Vostera. It declares its main objectives as the last-resort use of force to control fundamental terrorist and aetheric threats.</p>

                    <h2 id="5">Intelligence</h2>
                    <hr class="solid">
                    <p>The intelligence branch of the Academy is the branch dealing with collection of information, intelligence and recruitment of new spellweavers and gifted students to the academy's ranks. This branch is very secretive to its actual functioning as the information it deals with is highly sensitive. Many speculate that the actual function of the intelligence branch is functionally espionage.</p>

                    <h2 id="6">Rules Enforced and Philosophies</h2>
                    <hr class="solid">
                    <p>The academy's philosophies are quite clear. Magic and the arcane arts are a difficult, dangerous practice that requires teaching and proper instruction. Dangerous high level spells are guarded by the academy fiercely, and anyone seen using high level spells by Academy members will most likely be reprieved for it.</p>

                    <h2 id="7">Restricted spells</h2>
                    <hr class="solid">
                    <p>The academy forbids the use of the following spells through Ashirian legislation bill E90 - otherwise known as the Hudal bill;
                    <ul>
                        <li>Spells of necromantic nature, or that otherwise deal with resurrection</li>
                        <li>Spells of pyrolytic nature</li>
                        <li>Spells of divinatory nature</li>
                        <li>Spells of illusory nature</li>
                        <li>Spells of charming or otherwise psychic nature</li>
                        <li>Curses of any kind</li>
                    </ul>
                    The previous spells may only be used in time of war and by <a href="../organizations/peacekeepers.php" class="crosslink">peacekeepers</a> or other military personnel. The punishments for using these spells vary from the gravity of the spell, the intent of the spellweaver, and other mitigating and aggravating conditions surrounding the case. Both peacekeepers and imperial guards have the right to make arrests based on substantial suspicion of the use of spells under E90.
                    </p>

                    <h2 id="8">Symbols</h2>
                    <hr class="solid">
                    <p>The symbol of the academy is the trinity tree, a symbol of the reaches of the academy grasping across Vostera and into different aspect of Vosterians lives.</p>
