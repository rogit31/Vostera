        <h1>House Rules</h1>
        <article class="article-tile">
            <h2>Character Guidelines</h2>
            <hr class="solid">
            <ul>
                <li>Player characters need to have a reason to explore Vostera. This can be anything, like curiosity,
                    vengeance, religion, wanderlust, commerce, and so on.
                </li>
                <li>Player characters cannot be 'evil' or 'lone wolves'. They can be egotistical, hypocritical, dumb,
                    cruel, ignorant, righteous, or any other individual quality, but they must still be willing to work
                    in a group for one reason or another.
                </li>
                <li>Player characters need a backstory. Who are they and where did they come from? Their backstory
                    doesn't have to be complex or intricate, but it must exist to some extent. The process of building a
                    backstory is iterative, and can be changed along the way.
                </li>
                <li>Player characters <em>should</em> have an objective. It doesn't need to be grand or heroic, but
                    something to work towards is part of the hero's journey.
                </li>
                <li>Multi-classing is allowed, and so are custom spells, classes, sub-classes, and races with approval
                    from the DM. Players are encouraged to create and innovate as long as it doesn't break the pace of
                    the game.
                </li>
                <li>In the event a character dies, you are more than welcome to reroll a new character with DM approval.
                    You will not be set back in terms of levels, though of course, any items lost will not be
                    transferred.
                </li>
            </ul>
            <h2>Gameplay Modifications</h2>
            <hr class="solid">
            <ul>
                <li>Potions, poisons and similar items are considered a bonus action to use, and not an action.</li>
                <li>There are additional gameplay mechanics that exist in Vostera that do not exist in vanilla fifth
                    edition. See the "unstable" effect from liselos.
                </li>
                <li>Material components for spells that don't have a listed gold value are treated as non-existent. In
                    other words, most spells do not require your character to have access to a component pouch or
                    similar container.
                </li>
                <li>Keep in mind that casting spells is rarer in Vostera than in vanilla fifth edition. It is seen as a
                    rare and precious thing that only a lucky few are capable of. Those who are capable of it are wanted
                    for their power. Be careful who you cast a spell in front of.
                </li>
            </ul>
            <h2>Meta</h2>
            <hr class="solid">
            <ul>
                <li>Roleplay is encouraged, but not enforced. Do what you feel comfortable with at all times.
                    Inspiration may be awarded to moments of roleplay to reward players at the DM's discretion.
                </li>
                <li>Violent and adult themes are common to Vostera, but some things are just not fun no matter what. We
                    try to stay away from those things, notably homophobia, transphobia, ableism, colorism and sexual
                    violence. If you ever feel uncomfortable with anything happening, please feel free to interrupt or
                    to talk to me privately.
                </li>
                <li>Please try to be on time. We usually give ourselves a bit of time to set up before we start to have
                    some small talk, set up and get in the mood, but I still expect everyone to come on time.
                </li>
                <li>Sessions are scheduled on the discord at regular intervals, usually once every two weeks. Check the
                    discord regularly please.
                </li>
                <li>If for some reason or another a player cannot attend, we will still hold the session unless more
                    than half of the players can't make it. If the player is absent, so is the character. If you need to
                    take a longer break, talk to me and we can figure out a solution, usually involving either an
                    in-game reason for your character to leave or if you would like a new character, for it to die.
                </li>
            </ul>
